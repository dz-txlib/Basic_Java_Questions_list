
// 14. How do you check if a number is even or odd in Java?

class Que14 {

    public static void main(String[] args) {
        int a = 13;

        if (a % 2 == 0) {
            System.out.println("a is even ");

        }

        else {
            System.out.println("a is odd ");
        }

        /*
         * with the help of ' % ' modulo operator we can check what is
         * the remainder so if number%2 is 0 then thenumber is
         * divisable by 2 means it is positive else is negative
         */
    }
}
