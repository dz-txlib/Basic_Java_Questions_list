// 5. How can you convert a double to an int in Java?

class Que5 {
    public static void main(String[] args) {

        // With the help of explicit type casting :-it is used to
        // convert big data type to small data type

        double b = 343.33;
        int a = (int) b; // (int) it is used convert double to an int

        System.out.println(a);

    }

}