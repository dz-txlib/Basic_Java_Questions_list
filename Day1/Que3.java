// 3. Declare a constant variable named PI and assign it the value 3.14159.

class Que3 {
    public static void main(String[] args) {
        // we use final keyword to declare a constant variable
        final double a = 3.14159;
        // a = 14.11; --> cannot assign a value to a final variable

        System.out.println(a);

    }

}