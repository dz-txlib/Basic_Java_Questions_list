// 16. Create a program to print the ASCII values of all uppercase letters using a do-while loop.

public class Que16 {
    public static void main(String[] args) {

        for (int i = 65; i <= 90; i++) {
            char z = (char) i;
            System.out.println(" Ascii value of " + z + " is " + i);

        }

    }

}
