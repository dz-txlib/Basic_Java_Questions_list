// 11. Implement a program to print the even numbers from 2 to 20 using a while loop.

class Que11 {
    public static void main(String[] args) {
        int i = 2;
        while (i <= 20) {
            if (i % 2 == 0) {
                System.out.println(i);

            }
            i++;

        }

    }

}