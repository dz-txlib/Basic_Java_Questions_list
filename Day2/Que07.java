// 7. Write a program to check if a given character is a vowel or consonant.

class Que07 {

    public static void main(String[] args) {

        char c = 'a';
        switch (c) {
            case 'a':
                System.out.println(c + " is a vowel ");
                break;
            case 'e':
                System.out.println(c + " is a vowel ");
                break;
            case 'i':
                System.out.println(c + " is a vowel ");
                break;
            case 'o':
                System.out.println(c + " is a vowel ");
                break;
            case 'u':
                System.out.println(c + " is a vowel ");
                break;

            default:
                System.out.println(c + " is a consonant ");
                break;
        }
    }

}
