// 22. Write a program to calculate the sum of even numbers from 1 to 100.

class Que22 {
    public static void main(String[] args) {
        int sum = 0;
        // System.out.println(sum);
        for (int i = 0; i <= 100; i++) {
            sum += i;

        }
        System.out.println("The sum of 1 to 100 is " + sum);
    }

}
